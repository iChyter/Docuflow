create extension if not exists "uuid-ossp";

-- =====================================================
-- TABLA: PROFILES (extiende auth.users)
-- =====================================================
create table public.profiles (
    id uuid references auth.users on delete cascade not null primary key,
    username text unique not null,
    full_name text,
    role text default 'usuario' check (role in ('admin', 'colaborador', 'usuario')) not null,
    avatar_url text,
    preferences jsonb default '{}'::jsonb,
    created_at timestamptz default now(),
    updated_at timestamptz default now()
);

-- =====================================================
-- TABLA: DOCUMENTS
-- =====================================================
create table public.documents (
    id bigint generated by default as identity primary key,
    filename text not null,
    file_type text,
    file_path text not null,
    size bigint,
    uploaded_by uuid references public.profiles(id) on delete set null,
    uploaded_at timestamptz default now(),
    updated_at timestamptz,
    is_deleted boolean default false
);

-- =====================================================
-- TABLA: COMMENTS
-- =====================================================
create table public.comments (
    id bigint generated by default as identity primary key,
    content text not null,
    document_id bigint references public.documents(id) on delete cascade,
    author_id uuid references public.profiles(id) on delete set null,
    author_username text,
    is_task boolean default false,
    assignees text[] default '{}',
    completed boolean default false,
    created_at timestamptz default now(),
    updated_at timestamptz,
    last_edited_by text
);

-- =====================================================
-- TABLA: LOGS
-- =====================================================
create table public.logs (
    id bigint generated by default as identity primary key,
    action text not null,
    user_id uuid references public.profiles(id) on delete set null,
    username text,
    document_id bigint,
    target_username text,
    details text,
    ip_address text,
    created_at timestamptz default now()
);

-- =====================================================
-- TABLA: NOTIFICATIONS
-- =====================================================
create table public.notifications (
    id bigint generated by default as identity primary key,
    title text not null,
    message text,
    type text default 'info' check (type in ('info', 'warning', 'error', 'success', 'announcement')),
    priority text default 'medium' check (priority in ('low', 'medium', 'high', 'urgent')),
    user_id uuid references public.profiles(id) on delete cascade,
    is_global boolean default false,
    is_active boolean default true,
    metadata jsonb,
    created_at timestamptz default now(),
    expires_at timestamptz,
    created_by text
);

-- =====================================================
-- TABLA: REFRESH TOKENS (custom, para sesión larga)
-- =====================================================
create table public.refresh_tokens (
    id bigint generated by default as identity primary key,
    token text unique not null,
    user_id uuid references auth.users on delete cascade not null,
    expires_at timestamptz not null,
    revoked boolean default false,
    created_at timestamptz default now()
);

