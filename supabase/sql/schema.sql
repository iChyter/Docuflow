-- Obtener estadísticas del dashboard
create or replace function get_dashboard_stats()
returns jsonb
language plpgsql
security definer
as $$
declare
    result jsonb;
begin
    select jsonb_build_object(
        'total_files', (select count(*) from public.documents where is_deleted = false),
        'total_users', (select count(*) from public.profiles),
        'total_comments', (select count(*) from public.comments),
        'total_notifications', (select count(*) from public.notifications where is_active = true),
        'total_storage', (select coalesce(sum(size), 0) from public.documents where is_deleted = false),
        'recent_files', (
            select coalesce(jsonb_agg(
                jsonb_build_object(
                    'id', id,
                    'filename', filename,
                    'file_type', file_type,
                    'size', size,
                    'uploaded_at', uploaded_at
                )
            ), '[]'::jsonb)
            from (
                select id, filename, file_type, size, uploaded_at
                from public.documents 
                where is_deleted = false
                order by uploaded_at desc limit 5
            ) as recent
        ),
        'recent_activities', (
            select coalesce(jsonb_agg(
                jsonb_build_object(
                    'id', id,
                    'action', action,
                    'username', username,
                    'document_id', document_id,
                    'created_at', created_at
                )
            ), '[]'::jsonb)
            from (
                select id, action, username, document_id, created_at
                from public.logs
                order by created_at desc limit 10
            ) as recent
        )
    ) into result;

    return result;
end;
$$;

-- Buscar documentos
create or replace function search_documents(search_query text, limit_count int default 10)
returns table (
    id bigint,
    filename text,
    file_type text,
    size bigint,
    uploaded_by uuid,
    uploaded_at timestamptz
)
language plpgsql
security definer
as $$
begin
    return query
    select d.id, d.filename, d.file_type, d.size, d.uploaded_by, d.uploaded_at
    from public.documents d
    where d.is_deleted = false
    and d.filename ilike '%' || search_query || '%'
    order by d.uploaded_at desc
    limit limit_count;
end;
$$;

-- Completar tarea (comentario)
create or replace function complete_task(task_id bigint, completed boolean default true)
returns void
language plpgsql
security definer
as $$
begin
    update public.comments
    set completed = completed, updated_at = now()
    where id = task_id and is_task = true;
end;
$$;

-- Buscar documentos con filtros de fecha
create or replace function search_documents_with_filters(
    search_query text default null,
    start_date timestamptz default null,
    end_date timestamptz default null,
    limit_count int default 10
)
returns table (
    id bigint,
    filename text,
    file_type text,
    size bigint,
    uploaded_by uuid,
    uploaded_at timestamptz
)
language plpgsql
security definer
as $$
begin
    return query
    select d.id, d.filename, d.file_type, d.size, d.uploaded_by, d.uploaded_at
    from public.documents d
    where d.is_deleted = false
    and (
        search_query is null 
        or d.filename ilike '%' || search_query || '%'
    )
    and (
        start_date is null 
        or d.uploaded_at >= start_date
    )
    and (
        end_date is null 
        or d.uploaded_at <= end_date
    )
    order by d.uploaded_at desc
    limit limit_count;
end;
$$;

-- =====================================================
-- TRIGGER: Crear perfil automáticamente al registrar usuario
-- =====================================================
create or replace function handle_new_user()
returns trigger as $$
declare
    username_from_email text;
begin
    -- Generar username desde email
    username_from_email := split_part(new.email, '@', 1);
    
    -- Si el username ya existe, agregar sufijo único
    if exists (select 1 from public.profiles where username = username_from_email) then
        username_from_email := username_from_email || '_' || left(new.id::text, 8);
    end if;

    -- Crear perfil con rol 'usuario' por defecto (más restrictivo)
    insert into public.profiles (id, username, full_name, role)
    values (new.id, username_from_email, new.raw_user_meta_data->>'full_name', 'usuario');

    return new;
end;
$$ language plpgsql security definer;

create trigger on_auth_user_created
    after insert on auth.users
    for each row execute procedure handle_new_user();

