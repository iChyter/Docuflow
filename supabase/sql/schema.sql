as $$
declare
    user_role text;
begin
    select role into user_role
    from public.profiles
    where id = auth.uid();
    
    return user_role = 'admin';
end;
$$;

-- Función para verificar si el usuario puede ver logs (solo admin)
create or replace function public.can_view_logs()
returns boolean
language plpgsql
security definer
as $$
declare
    user_role text;
begin
    select role into user_role
    from public.profiles
    where id = auth.uid();
    
    return user_role = 'admin';
end;
